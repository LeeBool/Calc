#include <stdio.h>

float plus(float* arr) {
	float rst = 0;
	for (int i = 0; i < 3; i++) {
		rst += arr[i];
	}
	return rst;
}

float minus(float* arr) {
	float rst = arr[0];
	for (int i = 1; i < 3; i++) {
		rst -= arr[i];
	}
	return rst;
}

float multi(float* arr) {
	float rst = 1;
	for (int i = 0; i < 3; i++) {
		rst *= arr[i];
	}
	return rst;
}

float div(float* arr) {
	float rst = arr[0];
	for (int i = 1; i < 3; i++) {
		rst /= arr[i];
	}
	return rst;
}

